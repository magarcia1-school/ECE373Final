
public class Obstacle {
	
	public int X_Cord, Y_Cord;

	//constructors
	public Obstacle(int x_Cord, int y_Cord) {
		super();
		X_Cord = x_Cord;
		Y_Cord = y_Cord;
	}

	
	//getters and setters
	public int getX_Cord() {
		return X_Cord;
	}

	public void setX_Cord(int x_Cord) {
		X_Cord = x_Cord;
	}

	public int getY_Cord() {
		return Y_Cord;
	}

	public void setY_Cord(int y_Cord) {
		Y_Cord = y_Cord;
	}
	

}
